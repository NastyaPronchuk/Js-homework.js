function whatNumberIsIt(n){
  if(n==Infinity)                        
    n="Number.POSITIVE_INFINITY";
  else if(n==1.7976931348623157e+308)    
    n="Number.MAX_VALUE";
  else if(n==5e-324)                     
    n="Number.MIN_VALUE";
  else if(n==-Infinity)                  
    n="Number.NEGATIVE_INFINITY";
  else if(isNaN(n))                      
    n="Number.NaN";
 
return "Input number is "+n;
}

